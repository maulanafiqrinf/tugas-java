    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        if ("".equals(qty.getText())) {
            JOptionPane.showMessageDialog(null, "Lengkapi data!");
        } else {
            try {
                st = cn.createStatement();
                st.executeUpdate("insert into pemesanan set " + "kd_pesanan = '" + kd_pesanan.getText() + "', kd_menu='" + kd_menu.getText() + "', qty = '" + qty.getText() + "',sub_total = '" + sub_total.getText() + "';");
                tampiltmp();
                reset();
                jual();

                int total = 0;
                for (int i = 0; i < tb_tran.getRowCount(); i++) {
                    int amount = Integer.parseInt((String) tb_tran.getValueAt(i, 3));
                    total += amount;
                }
                this.total_harga.setText("" + total);

                JOptionPane.showMessageDialog(null, "Berhasil");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_tambahActionPerformed

    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        if ("".equals(kd_menu.getText()) || "".equals(harga.getText()) || "".equals(stok.getText()) || "".equals(qty.getText())) {
            JOptionPane.showMessageDialog(null, "lengkapi data");
        } else {
            Integer jumlah = Integer.parseInt(qty.getText());
            Integer stok = Integer.parseInt(this.stok.getText());
            if (jumlah > stok) {
                JOptionPane.showMessageDialog(null, "Stok hanya ada " + stok);
            } else {
                int a = Integer.parseInt(harga.getText());
                int b = Integer.parseInt(qty.getText());
                int hitung = a * b;
                sub_total.setText("" + hitung);
                tambah.setEnabled(true);
                selesai.setEnabled(true);
            }

        }
    }//GEN-LAST:event_hitungActionPerformed

