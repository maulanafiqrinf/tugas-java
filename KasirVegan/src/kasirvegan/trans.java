    private void jual() {
        try {
            Random angka = new Random();
            String angkaa = String.valueOf(angka.nextInt(999));
            String kode = "T" + angkaa;
            kd_pesanan.setText(kode);
            kd_pesanan.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void transaksi() {
        try {
            java.util.Date tgl = new java.util.Date();
            java.text.SimpleDateFormat kal = new java.text.SimpleDateFormat("yyMMdd");
            java.text.SimpleDateFormat tanggal = new java.text.SimpleDateFormat("yyyyMMdd");

            String sql = "Select max(kd_pembayaran) from pembayaran where tanggal =" + tanggal.format(tgl);
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                Long a = rs.getLong(1);
                if (a == 0) {
                    this.kd_pembayaran.setText(kal.format(tgl) + "0000" + (a + 1));
                } else {
                    this.kd_pembayaran.setText("" + (a + 1));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void barang() {
        nama_menu.removeAllItems();
        nama_menu.addItem("Pilih menu");
        try {
            st = cn.createStatement();
            rs = st.executeQuery("select * from menu");
            while (rs.next()) {
                nama_menu.addItem(rs.getString("nama_menu"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

