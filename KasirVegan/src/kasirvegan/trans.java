    private void tampiltmp() {
        try {
            st = cn.createStatement();
            tabmodel.getDataVector().removeAllElements();
            tabmodel.fireTableDataChanged();
            rs = st.executeQuery("select * from pemesanan");
            while (rs.next()) {
                Object[] data = {
                    rs.getString("kd_pesanan"),
                    rs.getString("kd_menu"),
                    rs.getString("qty"),
                    rs.getString("sub_total")
                };
                tabmodel.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

