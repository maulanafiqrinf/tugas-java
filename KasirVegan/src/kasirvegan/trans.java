    private void selesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selesaiActionPerformed
        // TODO add your handling code here:
        try {
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            int total = 0;
            st = cn.createStatement();
            rs = st.executeQuery("select * from pemesanan");
            while (rs.next()) {
                total = total + Integer.parseInt(rs.getString("sub_total"));
            }
            bayarr = Integer.parseInt(bayar.getText());
            totall = Integer.parseInt(this.total_harga.getText());
            if (bayarr < totall) {
                JOptionPane.showMessageDialog(null, "uang anda kuurang");
            } else {

                int kmbl = Integer.parseInt(bayar.getText()) - total;
                st.executeUpdate("insert into pembayaran values('" + kd_pembayaran.getText() + "','" + nama_pelanggan.getText() + "','" + no_hp.getText() + "','" + alamat.getText() + "','" + total + "','" + Integer.parseInt(bayar.getText()) + "','" + kembalian.getText() + "','" + now + "')");

            }
            try {
                int jawab = 0;
                if ((jawab = JOptionPane.showConfirmDialog(null, "Yakin ingin mengakhiri belanja?", "Konfirmasi", JOptionPane.YES_NO_OPTION)) == 0) {
                    String sql = "Truncate pemesanan";
                    st = cn.createStatement();
                    st.executeUpdate(sql);
                    tampiltmp();
                    transaksi();
                    nama_pelanggan.setText("");
                    this.total_harga.getText();
                    alamat.setText("");
                    no_hp.setText("");
                    bayar.setText("");
                    kembalian.setText("");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_selesaiActionPerformed

